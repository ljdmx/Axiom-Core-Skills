const fs = require('fs');
const path = require('path');

/**
 * Axiom Post-Mortem Generator v1.0
 * Purpose: Generates a high-fidelity PROJECT_RETRO.md based on Nexus data.
 */

const NEXUS_PATH = path.join(process.cwd(), 'PROJECT_NEXUS.json');
const RETRO_PATH = path.join(process.cwd(), 'PROJECT_RETRO.md');

function generate() {
    if (!fs.existsSync(NEXUS_PATH)) {
        console.error('[PostMortem] No Nexus found. Cannot generate retro.');
        return;
    }

    try {
        const nexus = JSON.parse(fs.readFileSync(NEXUS_PATH, 'utf8'));
        const date = new Date().toISOString().split('T')[0];

        const template = `# Project Retrospective — ${nexus.project_name || 'Axiom Project'}
Date: ${date}

## 1. Rams Scorecard
- **Overall Soul Score**: ${nexus.soul_scorecard?.overall || 'N/A'}/10
- **Understanding**: ${nexus.soul_scorecard?.axes?.understand || 0}%
- **Respect**: ${nexus.soul_scorecard?.axes?.respect || 0}%
- **Companionship**: ${nexus.soul_scorecard?.axes?.companion || 0}%

## 2. Technical Execution
- **Scale Tier**: ${nexus.scale_tier || 'Standard'}
- **Compliance**: ${nexus.compliance || 'Standard'}
- **Tech Stack**: ${nexus.stack?.join(', ') || 'N/A'}

## 3. Tech Debt Log
${nexus.tech_debt?.map(debt => `- [ ] ${debt}`).join('\n') || 'No major debt recorded.'}

## 4. Final Verdict
The project has been delivered according to the Axiom Core v10.0 Sovereign standards. 
Zero-Dead-End UX verified.

---
*Generated by Axiom post-mortem-generator.js*
`;

        fs.writeFileSync(RETRO_PATH, template, 'utf8');
        console.log('[PostMortem] PROJECT_RETRO.md generated successfully.');
    } catch (err) {
        console.error('[PostMortem] Generation failed:', err);
    }
}

generate();
